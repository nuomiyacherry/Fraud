/**
 * [getNationalPng 获取图片名字]
 * @cname  type 国家的cname
 * @return brief 图片的name
 */
export function getNationalPng(cname) {
  if (!cname) return ''
  if (!data.find(item => item.cn === cname)) return
  let brief = data.find(item => item.cn === cname).brief
  brief = brief.toLowerCase()
  return brief
}

/**
 * [getCountryImg 获取所有国家对应的图片]
 */
export function getCountryImg() {
  let json = {}
  data.forEach(item => {
    let name = item.brief.toLowerCase()
    let img = require(`public/countryFlag/${name}.png`)
    json[name] = {
      align: 'center',
      backgroundColor: {
        image: img
      }
    }
  })
  return json
}